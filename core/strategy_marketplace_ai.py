#!/usr/bin/env python3
"""
Strategy Marketplace AI Integration for Kingdom AI

This module provides integration between the Strategy Marketplace and ThothAI
for AI-powered strategy generation, analysis, and optimization.
"""

import logging
from datetime import datetime
from typing import Dict, Any, List

logger = logging.getLogger("KingdomAI.StrategyMarketplace.AI")

class StrategyMarketplaceAI:
    """
    AI integration for the Strategy Marketplace component.
    
    This class handles connections between the Strategy Marketplace and 
    ThothAI for AI-powered features.
    """
    
    async def _handle_strategy_generate(self, data: Dict[str, Any]) -> None:
        """
        Handle strategy generation requests using ThothAI.
        
        Args:
            data: Strategy generation parameters and requirements
        """
        try:
            if not data:
                await self._publish_error("generate", "No generation parameters provided")
                return
            
            # Check if ThothAI is available
            if not self.thoth_ai:
                await self._publish_error("generate", "ThothAI component is not available")
                return
            
            # Extract generation parameters
            user_id = data.get("user_id", "anonymous")
            strategy_type = data.get("strategy_type", "trend_following")
            markets = data.get("markets", ["crypto"])
            risk_level = data.get("risk_level", "medium")
            description = data.get("description", "")
            requirements = data.get("requirements", [])
            
            self.logger.info(f"Generating {strategy_type} strategy for {markets} with {risk_level} risk level")
            
            # Create prompt for ThothAI based on parameters
            prompt = self._create_strategy_generation_prompt(
                strategy_type, markets, risk_level, description, requirements
            )
            
            # Request code generation from ThothAI
            try:
                thoth_response = await self.event_bus.request("thoth.code.generate", {
                    "prompt": prompt,
                    "language": "python",
                    "task_type": "trading_strategy",
                    "model": "code"
                })
                
                # Process the response
                if thoth_response and "code" in thoth_response:
                    strategy_code = thoth_response["code"]
                    strategy_name = f"{strategy_type.title()} Strategy for {', '.join(m.title() for m in markets)}"
                    
                    # Create strategy metadata
                    strategy_data = {
                        "name": strategy_name,
                        "description": description or f"AI-generated {strategy_type} strategy for {', '.join(markets)} markets",
                        "long_description": f"This strategy was generated by ThothAI based on the following parameters:\n"
                                          f"- Strategy Type: {strategy_type}\n"
                                          f"- Markets: {', '.join(markets)}\n"
                                          f"- Risk Level: {risk_level}\n"
                                          f"- Requirements: {', '.join(requirements)}\n\n"
                                          f"The strategy uses {strategy_type} techniques to identify trading opportunities.",
                        "code": strategy_code,
                        "author": f"ThothAI for user {user_id}",
                        "category": strategy_type,
                        "risk_level": risk_level,
                        "markets": markets,
                        "tags": [strategy_type, "AI generated", *markets, risk_level],
                        "parameters": thoth_response.get("parameters", {}),
                        "requires_backtesting": True,
                        "ai_generated": True
                    }
                    
                    # Auto-detect required APIs from code
                    if "binance" in strategy_code.lower():
                        strategy_data["required_apis"] = ["binance"]
                    elif "coinbase" in strategy_code.lower():
                        strategy_data["required_apis"] = ["coinbase"]
                    elif "alpaca" in strategy_code.lower():
                        strategy_data["required_apis"] = ["alpaca"]
                    
                    # Publish the generated strategy
                    await self.publish_event("strategy.generated", {
                        "user_id": user_id,
                        "strategy": strategy_data,
                        "timestamp": datetime.now().isoformat()
                    })
                    
                    self.logger.info(f"Generated strategy {strategy_name} for user {user_id}")
                    
                    # Automatically submit the strategy to the marketplace if requested
                    if data.get("auto_submit", False):
                        await self._handle_strategy_submit({"strategy": strategy_data})
                        self.logger.info(f"Auto-submitted generated strategy {strategy_name} to marketplace")
                    
                else:
                    error_msg = "Failed to generate strategy code"
                    if thoth_response and "error" in thoth_response:
                        error_msg = f"ThothAI error: {thoth_response['error']}"
                    
                    await self._publish_error("generate", error_msg)
                    return
                
            except Exception as e:
                self.logger.error(f"Error during ThothAI code generation: {e}")
                await self._publish_error("generate", f"ThothAI code generation failed: {str(e)}")
                return
            
        except Exception as e:
            self.logger.error(f"Error handling strategy generation: {e}")
            await self._publish_error("generate", str(e))
    
